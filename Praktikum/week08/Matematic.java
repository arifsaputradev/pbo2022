public class Matematic {
    void pertambahan(int value1, int value2) {
        int result = value1+value2;
        System.out.println("Pertambahan: " + value1 + " + " + value2 + " = " + result);
    }
    void pengurangan(int value1, int value2) {
        int result = value1-value2;
        System.out.println("Pengurangan: " + value1 + " - " + value2 + " = " + result);
    }
    void perkalian(int value1, int value2) {
        int result = value1*value2;
        System.out.println("Perkalian: " + value1 + " * " + value2 + " = " + result);
    }
    void pembagian(int value1, int value2) {
        int result = value1/value2;
        System.out.println("Pembagian: " + value1 + " / " + value2 + " = " + result);
    }

    void pertambahan(float value1, float value2) {
        float result = value1+value2;
        System.out.println("Pertambahan: " + value1 + " + " + value2 + " = " + result);
    }
    void pengurangan(float value1, float value2) {
        float result = value1-value2;
        System.out.println("Pengurangan: " + value1 + " - " + value2 + " = " + result);
    }
    void perkalian(float value1, float value2) {
        float result = value1*value2;
        System.out.println("Perkalian: " + value1 + " * " + value2 + " = " + result);
    }
    void pembagian(float value1, float value2) {
        float result = value1/value2;
        System.out.println("Pembagian: " + value1 + " / " + value2 + " = " + result);
    }

    void pertambahan(double value1, double value2, double value3) {
        double result = value1+value2+value3;
        System.out.println("Pertambahan: " + value1 + " + " + value2 + " + " + value3 + " = " + result);
    }
    void pengurangan(double value1, double value2, double value3) {
        double result = value1-value2-value3;
        System.out.println("Pengurangan: " + value1 + " - " + value2 + " - " + value3 + " = " + result);
    }    
    void perkalian(double value1, double value2, double value3) {
        double result = value1*value2*value3;
        System.out.println("Perkalian: " + value1 + " * " + value2 + " * " + value3 + " = " + result);
    }    
    void pembagian(double value1, double value2, double value3) {
        double result = value1/value2/value3;
        System.out.println("Pembagian: " + value1 + " / " + value2 + " / " + value3 + " = " + result);
    }
}

